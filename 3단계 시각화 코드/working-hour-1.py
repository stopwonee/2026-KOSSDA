# 분석1 : 소득집단별 평균 하루 노동시간
# 최종 미니멀 버전
# ============================================================

import pandas as pd
import matplotlib.pyplot as plt
import numpy as np

# ------------------------------------------------------------
# 한글 폰트 설정 (Mac)
# ------------------------------------------------------------
plt.rcParams['font.family'] = 'AppleGothic'
plt.rcParams['axes.unicode_minus'] = False

# ------------------------------------------------------------
# 엑셀 파일 불러오기
# ------------------------------------------------------------

file_path = '분석 1_노동시간_t-test.xlsx'

df = pd.read_excel(file_path, sheet_name='descriptives')

# ------------------------------------------------------------
# 데이터 추출
# ------------------------------------------------------------

groups = df['income_group']
means = df['mean_worktime']
stds = df['sd_worktime']
ns = df['n']

# ------------------------------------------------------------
# 표준오차(Standard Error)
# ------------------------------------------------------------

ses = stds / np.sqrt(ns)

# ------------------------------------------------------------
# x축 위치 (중앙 정렬)
# ------------------------------------------------------------

x = [0.45, 0.65]

# ------------------------------------------------------------
# Figure 생성
# ------------------------------------------------------------

fig, ax = plt.subplots(figsize=(6, 4.2))

# ------------------------------------------------------------
# 배경
# ------------------------------------------------------------

fig.patch.set_facecolor('white')
ax.set_facecolor('white')

# ------------------------------------------------------------
# 점 + 에러바
# ------------------------------------------------------------

ax.errorbar(
    x[0],
    means[0],
    yerr=ses[0],
    fmt='o',
    markersize=7,
    capsize=5,
    linewidth=1.5,
    color='#1f77b4'
)

ax.errorbar(
    x[1],
    means[1],
    yerr=ses[1],
    fmt='o',
    markersize=7,
    capsize=5,
    linewidth=1.5,
    color='#d62728'
)

# ------------------------------------------------------------
# 평균값 표시
# ------------------------------------------------------------

ax.text(
    x[0],
    means[0] + 0.07,
    f'{means[0]:.2f}',
    ha='center',
    fontsize=11,
    color='#1f77b4'
)

ax.text(
    x[1],
    means[1] + 0.07,
    f'{means[1]:.2f}',
    ha='center',
    fontsize=11,
    color='#d62728'
)

# ------------------------------------------------------------
# 축 설정
# ------------------------------------------------------------

ax.set_xticks(x)
ax.set_xticklabels(groups, fontsize=11)

ax.set_ylabel('평균 노동시간(시간)', fontsize=12)

# 차이 과장 방지
ax.set_ylim(5.4, 6.8)

# x축 여백 제거
ax.set_xlim(0.38, 0.72)

# ------------------------------------------------------------
# 제목
# ------------------------------------------------------------

ax.set_title(
    '소득집단별 평균 하루 노동시간',
    fontsize=15,
    pad=12
)

# ------------------------------------------------------------
# 격자 최소화
# ------------------------------------------------------------

ax.grid(
    axis='y',
    linestyle='--',
    alpha=0.15
)

# ------------------------------------------------------------
# 테두리 제거
# ------------------------------------------------------------

ax.spines['top'].set_visible(False)
ax.spines['right'].set_visible(False)

# ------------------------------------------------------------
# 레이아웃 정리
# ------------------------------------------------------------

plt.tight_layout()

# ------------------------------------------------------------
# 저장
# ------------------------------------------------------------

plt.savefig(
    'analysis1_kossda_minimal.png',
    dpi=300,
    bbox_inches='tight'
)

# ------------------------------------------------------------
# 출력
# ------------------------------------------------------------

plt.show()