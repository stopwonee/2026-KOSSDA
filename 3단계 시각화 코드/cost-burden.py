# ============================================================
# KOSSDA 공모전용 시각화
# 분석4 : 여가 비용 부담 비교
# 100% Stacked Bar Chart
# ============================================================

import pandas as pd
import matplotlib.pyplot as plt
import numpy as np

# ------------------------------------------------------------
# 한글 폰트 설정 (Mac)
# ------------------------------------------------------------

plt.rcParams['font.family'] = 'AppleGothic'
plt.rcParams['axes.unicode_minus'] = False

# ------------------------------------------------------------
# 엑셀 파일 불러오기
# ------------------------------------------------------------

file_path = '분석4_여가비용부담_카이제곱결과.xlsx'

df = pd.read_excel(
    file_path,
    sheet_name='비용부담_표본수_비율'
)

# ------------------------------------------------------------
# 데이터 추출
# ------------------------------------------------------------

groups = df['소득집단']

burden = df['비용부담_있음_비율']
no_burden = df['비용부담_없음_비율']

# ------------------------------------------------------------
# Figure 생성
# ------------------------------------------------------------

fig, ax = plt.subplots(figsize=(6.5, 5))

# ------------------------------------------------------------
# 색상 설정
# ------------------------------------------------------------

burden_color = '#d62728'
no_burden_color = '#cfd8dc'

# ------------------------------------------------------------
# stacked bar 생성
# ------------------------------------------------------------

ax.bar(
    groups,
    no_burden,
    label='부담 없음',
    color=no_burden_color
)

ax.bar(
    groups,
    burden,
    bottom=no_burden,
    label='부담 있음',
    color=burden_color
)

# ------------------------------------------------------------
# 비율 텍스트 표시
# ------------------------------------------------------------

for i in range(len(groups)):

    # 부담 없음
    ax.text(
        i,
        no_burden[i] / 2,
        f'{no_burden[i]:.1f}%',
        ha='center',
        va='center',
        fontsize=10
    )

    # 부담 있음
    ax.text(
        i,
        no_burden[i] + burden[i] / 2,
        f'{burden[i]:.1f}%',
        ha='center',
        va='center',
        fontsize=10,
        color='white'
    )

# ------------------------------------------------------------
# y축 설정
# ------------------------------------------------------------

ax.set_ylim(0, 100)

ax.set_ylabel(
    '비율 (%)',
    fontsize=12
)

# ------------------------------------------------------------
# 제목
# ------------------------------------------------------------

ax.set_title(
    '소득집단별 여가 비용 부담 비교',
    fontsize=16,
    pad=15
)

# ------------------------------------------------------------
# 범례
# ------------------------------------------------------------

ax.legend(
    frameon=False,
    fontsize=11,
    loc='lower center',
    bbox_to_anchor=(0.5, -0.20),
    ncol=2
)

# ------------------------------------------------------------
# grid 최소화
# ------------------------------------------------------------

ax.grid(
    axis='y',
    linestyle='--',
    alpha=0.12
)

# ------------------------------------------------------------
# 테두리 제거
# ------------------------------------------------------------

ax.spines['top'].set_visible(False)
ax.spines['right'].set_visible(False)

# ------------------------------------------------------------
# 배경
# ------------------------------------------------------------

fig.patch.set_facecolor('white')
ax.set_facecolor('white')

# ------------------------------------------------------------
# 레이아웃 정리
# ------------------------------------------------------------

plt.tight_layout(rect=[0, 0.12, 1, 1])

# ------------------------------------------------------------
# 저장
# ------------------------------------------------------------

plt.savefig(
    'analysis4_leisure_cost_burden.png',
    dpi=300,
    bbox_inches='tight'
)

# ------------------------------------------------------------
# 출력
# ------------------------------------------------------------

plt.show()