# ============================================================
# KOSSDA 공모전용 시각화
# 분석1 : 노동시간 Boxplot + 평균점
# kor_data_20200102.sav 사용
# 최종 코드
# ============================================================

import pyreadstat
import pandas as pd
import matplotlib.pyplot as plt

# ------------------------------------------------------------
# 한글 폰트 설정 (Mac)
# ------------------------------------------------------------

plt.rcParams['font.family'] = 'AppleGothic'
plt.rcParams['axes.unicode_minus'] = False

# ------------------------------------------------------------
# SPSS 파일 불러오기
# ------------------------------------------------------------

file_path = 'kor_data_20200102.sav'

df, meta = pyreadstat.read_sav(file_path)

# ------------------------------------------------------------
# 실제 변수명
# ------------------------------------------------------------
# Q41 : 소득집단
# Q37 : 하루 노동시간
# ------------------------------------------------------------

income_var = 'Q41'
worktime_var = 'Q37'

# ------------------------------------------------------------
# 필요한 변수만 추출
# ------------------------------------------------------------

data = df[[income_var, worktime_var]].dropna()

# ------------------------------------------------------------
# 실제 코딩값 기준 그룹 분리
# ------------------------------------------------------------
# 1 = 저소득
# 2 = 비저소득
# ------------------------------------------------------------

low_income = data[data[income_var] == 1][worktime_var]
non_low_income = data[data[income_var] == 2][worktime_var]

# ------------------------------------------------------------
# 평균 계산
# ------------------------------------------------------------

low_mean = low_income.mean()
non_low_mean = non_low_income.mean()

# ------------------------------------------------------------
# Figure 생성
# ------------------------------------------------------------

fig, ax = plt.subplots(figsize=(6.5, 5))

# ------------------------------------------------------------
# Boxplot 생성
# ------------------------------------------------------------

box = ax.boxplot(
    [low_income, non_low_income],
    patch_artist=True,
    widths=0.5,
    showfliers=False
)

# ------------------------------------------------------------
# 색상 설정
# ------------------------------------------------------------

colors = ['#d62728', '#1f77b4']

for patch, color in zip(box['boxes'], colors):
    patch.set_facecolor(color)
    patch.set_alpha(0.25)

# ------------------------------------------------------------
# 평균점 추가
# ------------------------------------------------------------

ax.scatter(
    [1, 2],
    [low_mean, non_low_mean],
    color=colors,
    s=75,
    zorder=3
)

# ------------------------------------------------------------
# 평균값 텍스트
# ------------------------------------------------------------

ax.text(
    1,
    low_mean + 0.15,
    f'{low_mean:.2f}',
    ha='center',
    fontsize=11,
    color=colors[0]
)

ax.text(
    2,
    non_low_mean + 0.15,
    f'{non_low_mean:.2f}',
    ha='center',
    fontsize=11,
    color=colors[1]
)

# ------------------------------------------------------------
# x축
# ------------------------------------------------------------

ax.set_xticks([1, 2])

ax.set_xticklabels(
    ['저소득', '비저소득'],
    fontsize=12
)

# ------------------------------------------------------------
# y축
# ------------------------------------------------------------

ax.set_ylabel(
    '하루 노동시간(시간)',
    fontsize=12
)

# ------------------------------------------------------------
# 제목
# ------------------------------------------------------------

ax.set_title(
    '소득집단별 노동시간 분포',
    fontsize=16,
    pad=14
)

# ------------------------------------------------------------
# 격자 최소화
# ------------------------------------------------------------

ax.grid(
    axis='y',
    linestyle='--',
    alpha=0.15
)

# ------------------------------------------------------------
# 테두리 제거
# ------------------------------------------------------------

ax.spines['top'].set_visible(False)
ax.spines['right'].set_visible(False)

# ------------------------------------------------------------
# 배경색
# ------------------------------------------------------------

fig.patch.set_facecolor('white')
ax.set_facecolor('white')

# ------------------------------------------------------------
# 레이아웃 정리
# ------------------------------------------------------------

plt.tight_layout()

# ------------------------------------------------------------
# 저장
# ------------------------------------------------------------

plt.savefig(
    'analysis1_boxplot_final.png',
    dpi=300,
    bbox_inches='tight'
)

# ------------------------------------------------------------
# 출력
# ------------------------------------------------------------

plt.show()